# 1. Crie um programa que peça a maior nota que um
# jogo obteve, a menor nota que o jogo obteve, calcule e exiba a nota média
# do jogo.

nota1 = int(input('Qual a primeira nota ?'))
nota2 = int(input('Qual a segunda nota ?'))

media = (nota1 + nota2) / 2

print ('A média entre as duas notas é', media)
